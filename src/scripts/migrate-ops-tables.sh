#!/bin/bash
# Script de migration automatique des tables "ops" (audit trail + notifications)
# Utilise Supabase CLI si disponible, sinon affiche les instructions manuelles

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SQL_FILE="${SCRIPT_DIR}/ops_tables.sql"

if [ ! -f "$SQL_FILE" ]; then
  echo "❌ Fichier SQL introuvable: $SQL_FILE"
  exit 1
fi

echo "🔧 Migration des tables 'ops' (audit trail + notifications)"
echo ""

# Vérifier si Supabase CLI est disponible
if command -v supabase &> /dev/null; then
  echo "✅ Supabase CLI détecté"

  # Vérifier si on est dans un projet Supabase
  if [ -f "${SCRIPT_DIR}/../../supabase/config.toml" ] || [ -n "$SUPABASE_PROJECT_ID" ]; then
    echo "📤 Exécution via Supabase CLI..."

    if [ -n "$SUPABASE_PROJECT_ID" ]; then
      # Remote project
      supabase db execute -f "$SQL_FILE" --project-id "$SUPABASE_PROJECT_ID"
    else
      # Local project
      supabase db execute -f "$SQL_FILE"
    fi

    echo "✅ Migration terminée"
    exit 0
  else
    echo "⚠️  Projet Supabase non détecté localement"
  fi
fi

# Fallback: instructions manuelles
echo "⚠️  Supabase CLI non disponible ou projet non configuré"
echo ""
echo "📋 Instructions manuelles:"
echo ""
echo "1. Ouvre Supabase Dashboard → SQL Editor"
echo "2. Copie-colle le contenu de: $SQL_FILE"
echo "3. Exécute le script"
echo ""
echo "Ou via psql direct:"
echo "  psql \$DATABASE_URL -f $SQL_FILE"
echo ""
